package com.creatio.crm.language.basics;

public class DataTypeInJava {

	public static void main(String[] args) {
      // Primitive Data Types
		byte marks = 127;
		System.out.println(marks);
		
	    short landLineNumber = 12346;
	    System.out.println(landLineNumber);
	    
		int  monileNumber = 704878656;
		System.out.println(monileNumber);
		
		long phoneNumber = 345678901123457162L;
		System.out.println(phoneNumber);
		
		float value = 3.1234f;
		System.out.println(value);
		
		
		double value1 = 32.3212341d;
		System.out.println(value1);
		
		boolean validAge = true;
		System.out.println(validAge);
		
		char grade = 'A';
		System.out.println(grade);
		
		//Non-Primitive Data Types
		
		String name= "Niraj";
		
		
	}

}
